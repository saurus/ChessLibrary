package org.saurus.chess.pgn;

public class MovePair {
	private Move white;
	private Move black;
	
	public Move getWhite() {
		return white;
	}
	
	public void setWhite(Move white) {
		this.white = white;
	}
	
	public Move getBlack() {
		return black;
	}
	
	public void setBlack(Move black) {
		this.black = black;
	}
}